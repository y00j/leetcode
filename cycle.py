def detect_cycle_iteratively(graph):
    UNVISITED = 0
    VISITING = 1
    VISITED = 2

    for node in graph:
        graph[node] = [str(num) for num in graph[node]]
    

    state = {node: UNVISITED for node in graph}

    def iterative_dfs(start_node):
        
        stack = [(start_node, iter(graph[start_node]))]
        state[start_node] = VISITING

        while stack:
            node, neighbors = stack[-1]  # Get the current node and its neighbors iterator
            try:
                neighbor = next(neighbors)  # Get the next neighbor
                if state[neighbor] == VISITING:
                    return True  # Found a cycle
                if state[neighbor] == UNVISITED:
                    state[neighbor] = VISITING
                    stack.append((neighbor, iter(graph[neighbor])))  # Push the neighbor and its neighbors iterator
            except StopIteration:
                stack.pop()  # No more neighbors to process
                state[node] = VISITED

        return False




    for node in graph:
        if state[node] == UNVISITED:
            if iterative_dfs(node):
                return True

    return False

